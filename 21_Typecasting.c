// Typecasting
#include <stdio.h>
int main()
{
    int a = 13;
    float b = 49.83;
    float c = 57 / 9;
    float d = (float)57 / 9;
    printf("The value of a is %d\n", a);
    printf("The value of b is %d\n", (int)b);
    printf("The value of c is %f\n", c);
    printf("The value of d is %f\n", d);
    return 0;
}