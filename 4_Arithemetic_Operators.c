//Arithemetic Operators
#include <stdio.h>
int main()
{
    int a, b;
    a = 34;
    b = 6;
    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a % b = %d\n", a % b);
    printf("---------------------------------\n");

    int A;
    float B;
    A = 34;
    B = 6.32;
    printf("A / B = %f\n", A / B);
    printf("A + B = %f\n", A + B);
    printf("A - B = %f\n", A - B);
    printf("A * B = %f\n", A * B);
    return 0;
}
