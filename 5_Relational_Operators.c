//Relational Operators
#include <stdio.h>
int main()
{
    int a, b;
    a = 34;
    b = 6;
    printf("a==b = %d\n", a == b);
    int c, d;
    c = 4;
    d = 6;
    printf("c!=d = %d\n", c != d);
    int e, f;
    e = 7;
    f = 7;
    printf("e==f = %d\n", e == f);

    return 0;
}