//Size of Data types
#include <stdio.h>
int main()
{
    printf("The size of int is %lu\n", sizeof(int));
    printf("The size of float is %lu\n", sizeof(float));
    printf("The size of char is %lu\n", sizeof(char));
    printf("The size of unsigned char is %lu\n", sizeof(unsigned char));
    return 0;
}
